# Crie um programa que leia dois números e mostre a soma entre eles.

# Defining the first and second number variables.  //  Definindo as variáveis do primeiro e segundo valor.
first_number = int(input('Enter a value: '))  
second_number = int(input('Enter another value: '))  

# Calculating the sum.  //  Calculando a soma.
sum_result = first_number + second_number  

# Displaying the final result.  //  Exibindo o resultado final.
print('The sum between {} and {} is equal to {}.'.format(first_number, second_number, sum_result))

